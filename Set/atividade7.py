# Enunciado: Crie dois conjuntos:
# ● grupo_a com os elementos {"João", "Maria", "Pedro"}
# ● grupo_b com os elementos {"Maria", "Ana", "Carlos"}
# Faça a união desses dois conjuntos e imprima o resultado.

grupo_a = {"João", "Maria", "Pedro"}
grupo_b = {"Maria", "Ana", "Carlos"}
uniao = grupo_a.union(grupo_b)
print(uniao)